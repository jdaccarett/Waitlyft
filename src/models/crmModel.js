// -- Model
// This is where we're going to create our schema and connect to mongodb.
import mongoose from 'mongoose';

const Schema = mongoose.Schema;

export const WaitListSchema = new Schema({
    restaurant: {
        type: String,
        required: 'Enter your restaurant name'
    },
    location: {
        longitude: {
          type: Number,
          required: 'Enter a longitude of restaurant'
        },
        latitude: {
          type: Number,
          required: 'Enter a latitude of restaurant'
        }
    },
    tablesWaitListed: { type : Array , "default" : [] }
});
